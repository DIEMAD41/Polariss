paqueteSeleccion.addEventListener('click', () => {
    sincronizarLocalStorage();
});

function sincronizarLocalStorage() {
    localStorage.setItem('opcPaquete', JSON.stringify(Paquetes));
}

function dibujarArticulos() {
    limpiarPaquetes();

    Paquetes.forEach(p => {

        let rebaja = 'descuento';
        if (p.rebaja == '') {
            rebaja = 'descuento-non';
        } 

        let oferta = 'oferta';
        let ofertaText = '¡Oferta exclusiva!';
        if (p.oferta == false) {
            oferta = 'oferta-non';
            ofertaText = '';
        }

        const formHTML = document.createElement('article');
        formHTML.setAttribute('class', 'paquete');
        formHTML.setAttribute('id', 'paquete-seleccion');

        formHTML.innerHTML = `<div class="up"> 
        <img src="${p.imagen}">
        <div class="info">
            <p class="titulo-paquete"> ${p.nombre} </p> 
            <p class="des-paquete"> ${p.des} </p>
            
            <div class="precios">
                <p class="texto-paquete"> Precio: </p>
                <p class="precio-paquete"> $${p.precio} MXN </p>
                <p class="${rebaja}"> ${p.rebaja} </p>
                <p class="${oferta}"> ${ofertaText} </p>
            </div>

            <div class="down"> 
                <div>
                    <p class="texto-paquete"> ${p.hotel.nombreHabitacionEstandar}: </p>
                    <p class="precio-hotel"> $${p.hotel.precioHabitacionEstandar} MXN </p>
                </div>
                <div>
                    <p class="texto-paquete"> ${p.hotel.nombreHabitacionPremium}: </p>
                    <p class="precio-hotel-p"> $${p.hotel.precioHabitacionPremium} MXN </p>
                </div>
            </div>
        </div>
    </div>`;

    paquetesSeccion.appendChild(formHTML);

    });
}

